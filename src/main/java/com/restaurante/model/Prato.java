package com.restaurante.model;

public class Prato {
	
	private int id;	
	private String nome;
	private String descricao;
	private double preco;

	public int getId() {
		return id;
	}

	public String getNome() {
		return nome;
	}
	
	public String getDescricao() {
		return descricao;
	}
	
	public double getPreco() {
		return preco;
	}
	
}
